
#include <stdio.h>

int main() {
    int age = 25;               // Integer
    float height = 5.9;         // Floating-point number
    char grade = 'A';           // Single character
    double pi = 3.1415926535;   // Double-precision floating-point number

    printf("Age: %d\n", age);
    printf("Height: %.2f\n", height);
    printf("Grade: %c\n", grade);
    printf("Pi: %.10f\n", pi);

    return 0;
}
